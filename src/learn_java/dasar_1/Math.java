package learn_java.dasar_1;

public class Math {
    public static void main(String[] args) {
        int x = 10;

        x += 10;
        System.out.println(x);

        x -= 10;
        System.out.println(x);
        
        x *= 10;
        System.out.println(x);

        x++;
        System.out.println(x);

        x--;
        System.out.println(x);
    }
}
